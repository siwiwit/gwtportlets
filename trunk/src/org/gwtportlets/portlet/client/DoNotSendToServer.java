package org.gwtportlets.portlet.client;

import java.lang.annotation.ElementType;
import java.lang.annotation.Target;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;

/**
 * {@link WidgetFactory} fields tagged with this annotation are nulled before
 * the factory is sent to the server on refresh. Use on data fields that are
 * populated on the server (e.g. data to display) but that are not needed
 * to refresh the widget.
 */
@Target(ElementType.FIELD)
@Retention(RetentionPolicy.RUNTIME)
public @interface DoNotSendToServer {
}
